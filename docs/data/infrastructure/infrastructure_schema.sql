CREATE TABLE settlements(
  settlement_id TEXT PRIMARY KEY,
  name TEXT,
  type TEXT,
  kind TEXT,
  subject TEXT,
  federal_district TEXT,
  municipal TEXT,
  rural_settlement TEXT,
  lat REAL,
  lon REAL,
  population_2010 INTEGER,
  population_2020 INTEGER,
  children_2020 INTEGER,
  growth_pct REAL,
  children_share_pct REAL,
  density TEXT,
  agglomeration TEXT,
  agglomeration_center TEXT,
  oktmo TEXT,
  rural_center INTEGER,
  infrastructure_score REAL,
  engineering_score REAL,
  social_score REAL,
  demographic_score REAL,
  confidence_score REAL,
  class_code TEXT,
  class_label TEXT,
  main_deficit TEXT,
  roads_score REAL,
  power_score REAL,
  gas_score REAL,
  water_score REAL,
  sewer_score REAL,
  digital_score REAL,
  education_score REAL,
  medical_score REAL,
  services_score REAL
