CREATE TABLE region_summary(
  subject TEXT PRIMARY KEY,
  federal_district TEXT,
  settlements INTEGER,
  municipalities INTEGER,
  population INTEGER,
  children INTEGER,
  avg_score REAL,
  engineering_score REAL,
  social_score REAL,
  data_confidence_note TEXT
